 #A program which takes Base and Height of any Triangle as input and prints Area of that 
 # Triangle.

b = float(input("Enter Base of Triangle   = "))
h = float(input("Enter Height of Triangle = "))

ar = (b*h)/2

print("--------------------------------------------------------------")
print("Area of Triangle having {0} as Base and {1} as Height = {2}".format(b,h,ar))
print("--------------------------------------------------------------")
